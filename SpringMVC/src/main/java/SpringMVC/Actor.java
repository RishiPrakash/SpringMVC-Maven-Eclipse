package SpringMVC;

public class Actor {

	
		 private String name;
		 private String favMovie;

		  public void setname(String name){
		    this.name = name;
		  }
		  public void setfavMovie(String movie){
		    this.favMovie = movie;
		  }

		  public String getname(){
		    return this.name;
		  }
		 public String getfavMovie(){
		    return this.favMovie;
		  }
		
	
	
}
